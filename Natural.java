public class Natural {
    public static void main(String args[]){
         //Printing number from 100 to 200 natural using while loop.
        int i = 100;
        while(i>=100 && i<=200){
            System.out.println(i);
            i++;
        }

        //Printing N natural no.s up to 10 using do while loop.
        int a = 1;
        do{
            System.out.println(a);
            a++;
        }while(a<=10);
    }
    
}
